    (function($){
        var recommend = {
            option: {
                f_form: '#recommend_form',
                f_prof: '#filter_profession',
                f_state: '#filter_state',
                f_city: '#filter_city',
            },
            init: function(){
                var _this=this;
                _this.filter();
                _this.associated_select();
            },
            associated_select: function(){
                var _this=this,o=_this.option;
                $(o.f_state).on('change', function(){
                    var state_id = $(this).val();
                    if(typeof(state_id)!='undefined' && state_id!='-1'){
                        var city = $.ajax({
                            url: 'http://sta.seogreen.net/index.php?action=recommend',
                            type: 'post',
                            dataType: 'json',
                            data: {'do':'get_city','state_id':state_id}
                        });
                        city.done(function(json){
                            _this.set_city_select(json['data']);
                        })
                            .fail(function(error){
                                console.error(error);
                            });
                    } else {
                        $(o.f_city).empty()
                            .prepend($('<option value="-1">Сначала выберите штат</option>'))
                            .prop('disabled', true);
                    }
                });
            },
            set_city_select: function(city){
                var _this=this,o=_this.option;
                var f_c = $(o.f_city), fc_o;
                f_c.empty();
                if(f_c.prop) {fc_o = f_c.prop('options');} else {a_o = f_c.attr('options');}
                $.each(city, function(key, value) {
                    if(key=='0'){key='-1';}
                    fc_o[fc_o.length]= new Option(value,key);
                });
                f_c.prop('disabled', false);
            },
            filter: function(){
                var _this=this,o=_this.option,form,loc,url=new Url;
                $(o.f_form).on('submit',function(e){
                    e.preventDefault();
                    loc = '';
                    form=$(this).serializeArray().reduce(function(obj,item){obj[item.name]=item.value;return obj;},{});
                    for(var k in form){
                        if (form.hasOwnProperty(k)){
                            if(form[k] != '-1'){
                                loc += ';'+ k +'=' + form[k];
                            }
                        }
                    }
                    loc = url.protocol + '://' + url.host + '' + url.path + ( loc.indexOf(';') == 0 ? '?' + loc.substring(1)  : loc ) ;
                    if(decodeURIComponent(loc) != decodeURIComponent(url.toString())){
                        window.location = loc;
                    }
                });
            }
        };
        $(document).ready(function(){
            recommend.init();
        });
    })(jQuery);
